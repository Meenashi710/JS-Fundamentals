//Relational and Equality Operators

//1.Relational or conditional operator

/*1.>- more than
2. <- less than
3. >= more than equal
4. <= less than equal */

var result = 4 <= 5
console.log(result)


//2.Euality Operator

var x = 1
console.log(x == 1)
console.log(x =='1')  // loose comparison (value on the right is equal to value on the left, not checking data type)
console.log(x === '1') // strict comparison (checks the data type of the value also)